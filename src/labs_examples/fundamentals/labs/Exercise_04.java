package labs_examples.fundamentals.labs;

/**
 * Fundamentals Exercise 4: My Second Program
 *
 *      Write the necessary code to display, declare and print each of Java's primitive data types.
 *      Please declare a single variable of each type, give it an appropriate value, then print that variable.
 *
 */

public class Exercise_04 {

    public static void main(String[] args) {

        int i = 1;
        System.out.println("int i is: " + i);
        boolean tOrf = false;
        System.out.println("boolean tOrf is: " + tOrf);
        char ltr = 'b';
        System.out.println("char ltr is: " + ltr);
        byte byt = 126;
        System.out.println("Byte byt is: " + byt);
        short shr = 31000;
        System.out.println("Short shr is: " + shr);
        float flt = 1.12121f;
        System.out.println("float flt is: " + flt);
        double dub = 1.8697869687;
        System.out.println("Double dub is: " +dub);
        long lng = 1293810928310L;
        System.out.println("Long lng is: " + lng);

    }

}
